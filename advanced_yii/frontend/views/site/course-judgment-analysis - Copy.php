<strong>OBJECTIVE OF THE COURSE:</strong>
Judgment Analysis is a basic level research course. The main objective of this short term course is to get  law students aware of various fixed data points in judgments. These data points are always usefull for any research analysis that can pivot around the judgment or the conclusion of any case. The data point can be gatherd by the research analyst and relate it with the conclusion or the out come of the judgments. Some of the standards data point that are always available may be defined as  Judgment Title, Judgment Date, Citation, acts, name of advocates anf Judges, etc and conclusion.
