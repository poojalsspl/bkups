<h4><strong>INTRODUCTION</strong></h4>
<hr>
<p>
	Judgment Analysis is the basic level course offered by Adlaw. The main objective of this course is to make students aware of the various fixed data points (eg. Advocates name, judges name, description, disposition etc.) that a judgment contains. Students after going through the judgment can allocate the various fixed data points. This course is for the students who have less time to spare in any course. Through this a student may develop a habit of studying the judgment and understanding it.
</p>

<p>
<h4><strong>OBJECTIVE OF THE COURSE:</strong></h4>   
<hr>     
The main objective of this short term course is to get  law students aware of various fixed data points in judgments. These data points are always usefull for any research analysis that can pivot around the judgment or the conclusion of any case. The data point can be gatherd by the research analyst and relate it with the conclusion or the out come of the judgments. Some of the standards data point that are always available may be defined as  Judgment Title, Judgment Date, Citation, acts, dispostion, description, etc.</p>
<p>
	<h4><strong>SYLLABUS:</strong></h4>
	Allocating Fixed Legal Data Points from the Judgments
</p>
<p>
    <h4><strong>COURSE CONTENT:</strong></h4><hr>
        <ol type="i">
          <li>1500-3000 Judgments will be given.</li>
          <li>For allocating fixed data points student have to follow certain rules which are as follows:
          <ol type="a">
          	<li><b>Case Number -</b> The number of the case as provided in the judgment</li>
          	<li><b>Jurisdiction of the Court -</b> Original, appeallate, Writs, Special Leave Jurisdiction according to the case.</li>
          	<li><b>Judges Name -</b> As provided in the case</li>
          	<li><b>Bench-</b>
          		<ul>
          			<li>Name of One judge - Single Bench</li>
          			<li>Name of Two Judges - Division Bench</li>
          			<li>Name of Three or more Judges - Full Bench</li>
          			<li>Constitution Bench- Constitution Bench</li>
          		</ul>
            </li>
            <li><b>Advocates Name - </b>As provided in the case for both the parties</li>
            <li><b>Citation - </b>As provided in the case</li>
            <li><b>Description - </b>As per the acts and section imposed in the case</li>
            <li><b>Disposition - </b>As per the decision of the case</li>
          </ol>
          </li>
        </ol>
        <b>In case any of the data points is missing in full judgment text , student has to search on web and generate the missing data points. URL of the website to be submitted in the data point remark section of the interface. Extra marks will be given for this.</b>
</p>
<p>
    <h4><strong>COURSE SUMMARY:</strong></h4>
    <hr>
    <ol type="i">
        <li>Judgments delivered from various High courts and Supreme Court of India is provided for the research under this course to each student</li>
        <li>List of all the Indian bare acts are also provided for previewing</li>
        <li>Since Act and section imposed is the fulcrum of the judgments, the list of various act with sections are provided to each students doing research on judgment.</li>
        <li>A user-friendly web based interface is designed for each student to generate the various pre defined legal data points of judgment. The interface also provides features to generate custom data point by each research that enable them to have a detail research analysis of judgments</li>
        <li>Analysing judgments delivered by various High Court and Supreme Court of India.</li>
        <li>Students have to fill the related fields after studying the judgments given in judgment body.</li>
    </ol>
</p>
