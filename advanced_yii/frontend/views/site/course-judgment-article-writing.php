<h4><strong>INTRODUCTION</strong></h4>
<hr>
<p>
Judgment Analysis with Article Writing is the basic level course offered by Adlaw. This course revolves around two objective that is- firstly, making students knowledgeable regarding fixed data points (eg. Advocates name, judges name, description, disposition etc.) as well as Article Writing. Article Writing in law field has its own importance, which keeps a student updated in the world of law. Topics are provided in the course content where a student can select according to his interest.
</p>

<p>
<h4><strong>OBJECTIVE OF THE COURSE:</strong></h4>   
<hr>     
The main objective of this course is to get  law students aware of various fixed data points in judgments. These data points are always usefull for any research analysis that can pivot around the judgment or the conclusion of any case. The data point can be gatherd by the research analyst and relate it with the conclusion or the out come of the judgments. And also to teach the students the art of Legal Article Writing.</p>
<p>
	<h4><strong>SYLLABUS:</strong></h4>
  <ol type="i">
	<li>Allocating Fixed Legal Data Points from the Judgments</li>
  <li>Acts and Section imposed</li>
  <li>Legal Article Writing</li>
  </ol>
</p>
<p>
    <h4><strong>COURSE CONTENT:</strong></h4><hr>
        <ol type="i">
          <li>3000-5000 Judgments will be given.</li>
          <li>For allocating fixed data points student have to follow certain rules which are as follows:
          <ol type="a">
          	<li><b>Case Number -</b> The number of the case as provided in the judgment</li>
          	<li><b>Jurisdiction of the Court -</b> Original, appeallate, Writs, Special Leave Jurisdiction according to the case.</li>
          	<li><b>Judges Name -</b>As provided in the case.</li>
          	<li><b>Bench-</b>
          		<ul>
          			<li>Name of One judge - Single Bench</li>
          			<li>Name of Two Judges - Division Bench</li>
          			<li>Name of Three or more Judges - Full Bench</li>
          			<li>Constitution Bench- Constitution Bench</li>
          		</ul>
            </li>
            <li><b>Advocates Name - </b>As provided in the case for both the parties</li>
            <li><b>Citation - </b>As provided in the case for both the parties</li>
            <li><b>Description - </b>As per the acts and section imposed in the case</li>
            <li><b>Disposition - </b>As per the decision of the case</li>
          </ol>
          </li>
          <li>Student have to write 12-16 Articles during the Course period.</li>
          <li>10 topics for Article Writing will be provided to the student while the remaining the student have to select. </li>
          <li>For Article Writing there are some rules which the student have to follow, which are as follows:
            <ol type="a">
            <li>Title of the Article</li>
            <li>Body of the Article which shall include 2-3 paragraphs</li>
            <li>Conclusion (Ending paragraph of the article with the opinion or recommendation, anticipation or an appeal)</li>
            <li>Date and Name of the Writer</li>

          </ol>
          </li>
          <li>Mention the name of the Acts and sections imposed. </li>
          
         
        </ol>
        <b>In case any of the data points is missing in full judgment text , student has to search on web and generate the missing data points. URL of the website to be submitted in the data point remark section of the interface. Extra marks will be given for this.</b>
</p>
<p>
    <h4><strong>COURSE SUMMARY:</strong></h4>
