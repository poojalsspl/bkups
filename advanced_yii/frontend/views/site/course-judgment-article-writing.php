        <li>List of all the Indian bare acts are also provided for previewing</li>
        <li>Since Act and section imposed is the fulcrum of the judgments, the list of various act with sections are provided to each students doing research on judgment.</li>
        <li>A user-friendly web based interface is designed for each student to generate the various pre defined legal data points of judgment. The interface also provides features to generate custom data point by each research that enable them to have a detail research analysis of judgments</li>
        <li>Analysing judgments delivered by various High Court and Supreme Court of India.</li>
        <li>Students have to fill the related fields after studying the judgments given in judgment body.</li>
        <li>Writing Legal Articles on topics given .</li>
    </ol>
</p>
