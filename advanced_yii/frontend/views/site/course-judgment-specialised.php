<p>
    <h4><strong>COURSE CONTENT:</strong></h4><hr>
        <ol type="i">
          <li>Judgments will be given as follows:
          <ol type="a">
          
              <li>Group 1: 2500-4000</li>
              <li>Group 2: 3500-5000</li>
              <li>Group 3: 2000-4000</li>
              <li>Group 4: 2000-4000</li>
          </ol>    
          </li>
          <li>For allocating fixed data points student have to follow certain rules which are as follows:
          <ol type="a">
          	<li><b>Case Number -</b> The number of the case as provided in the judgment</li>
          	<li><b>Jurisdiction of the Court -</b>Original, appeallate, Writs, Special Leave Jurisdiction according to the case.</li>
          	<li><b>Judges Name -</b> As provided in the case.</li>
          	<li><b>Bench-</b>
          		<ul>
          			<li>Name of One judge - Single Bench</li>
          			<li>Name of Two Judges - Division Bench</li>
          			<li>Name of Three or more Judges- Full Bench</li>
          			<li>Constitution Bench- Constitution Bench</li>
          		</ul>
            </li>
            <li><b>Advocates Name - </b>As provided in the case for both the parties</li>
            <li><b>Citation - </b>As provided in the case</li>
            <li><b>Description - </b>As per the acts and section imposed in the case</li>
            <li><b>Disposition - </b>As per the decision of the case</li>
          </ol>
          </li>
          <li>For Abstract writing there are some rules which the student have to follow, which are as follows:
          <ol type="a">
            <li>Name of the Act and Sections must be mentioned.</li>  
            <li>Facts of the Case in brief.</li>  
            <li>Decision of the Lower Courts, High Courts, Tribunals and Supreme Court.</li>  
            <li>Abstract must be written in past tense.</li>  
          </ol>
          </li>
          <li>Judgment Elements consists of:
          <ol type="a">
            <li>Facts of the Case: Matter in issue should be mentioned</li>
            <li>Ruling: Mentioning decision of lower courts and Tribunals if any</li>
            <li>Arguments: Arguments of the advocates</li>
            <li>Cases Referred: Mention the name and description of the cases referred.Variable Data Points</li>
            <li>Decision and Reason: Decision and the reason of that decision should be mentioned of the Court under which the case was going on.</li>
            <li>Everything must be plotted according to the elements. No change should be done during plotting of the elements.</li>
          </ol>  
          </li>
          <li>No. of Judgment Elements are as follows:
            <ol type="a">
              <li>Group 1: nil</li>
              <li>Group 2: 15-20</li>
              <li>Group 3 nil</li>
              <li>Group 4: nil</li>
            </ol>
          </li>
          <li>Mention the name of the Acts and sections imposed (Group 1 & 2 )</li>
          <li>Decscribe the Acts and sections imposed (Group 3 & 4 )</li>
          <li>Student can present their own views and thoughts about the particular case which relates to defence and accuisition</li>
          <li>Student have to draft Legal Documents during the Course period.</li>
          <li>The no.of Drafting differs according to following groups.
          <ol type="a">
              <li>Group 1: nil</li>
              <li>Group 2: 10-12</li>
              <li>Group 3: nil</li>
              <li>Group 4: 12-16</li>
            </ol>
          </li>
          <li>The topics for Drafting will be provided to the student </li>
          <li>Re-drafting of Judgment elements:
          <ol type="a">
              <li>The Judgment elements as percribed in  point iv will be re-drafted </li>
              <li>Points to remember here is- the drafting need to be done in past tense.</li>
              <li>Re-drafting has to be done literally.</li>
          </ol>
          </li>
          <li>The no.of Re-drafting differs according to following groups.
          <ol type="a">
            <li>Group 1: nil</li>
            <li>Group 2: 12-16</li>
            <li>Group 3: 10-12</li>
            <li>Group 4: 10-12</li>
          </ol>
          </li>
          <li>The no.of Article Writing as per the Group are as follows:
          <ol type="a">
            <li>Group 1: 12-16</li>
            <li>Group 2: nil</li>
            <li>Group 3: 12-16</li>
            <li>Group 4: 15-20</li>
          </ol>
          </li>
          <li>For Article Writing there are some rules which the student have to follow, which are as follows:
          <ol type="a">
            <li>Title of the Article</li>
            <li>Body of the Article which shall include 2-3 paragraphs</li>
            <li>Conclusion (Ending paragraph of the article with the opinion or recommendation, anticipation or an appeal)</li>
            <li>Date and Name of the Writer</li>
          </ol>  
          </li>
          <li>Cases referred in the judgment- Describe the facts of the cases referred in the judgment</li>
        </ol>
        <b>In case any of the data points is missing in full judgment text , student has to search on web and generate the missing data points. URL of the website to be submitted in the data point remark section of the interface. Extra marks will be given for this.</b>
</p>
