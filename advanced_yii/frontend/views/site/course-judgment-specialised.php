<p>
Specialised Judgment Analysis is the advance level course offered by Adlaw where a student can select the syllabus as per his area of interest in law. He can select two or three area of law where he want to specialise his interest. This is the most impactive course with the unique feature of specialisation. This enables a student to understand his area of interest in law and specialising it.
</p>

<p>
<h4><strong>OBJECTIVE OF THE COURSE:</strong></h4>   
<hr>     
The main objective of this course is to get  law students aware of various fixed data points in judgments. These data points are always usefull for any research analysis that can pivot around the judgment or the conclusion of any case. This course being the advance course brings for the student with the unique feature of specialisation all the necessary parameters that a law student will crave to learn.</p>
