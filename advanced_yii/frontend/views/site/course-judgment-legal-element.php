<h4><strong>INTRODUCTION</strong></h4>
<hr>
<p>
Legal Element Precis Analysis is the intermediate level of course offered by Adlaw. This course is the extended version of Judgment Precis Analysis  with the unique feature of Judgment elements (discussed in course content point v), which demands intense knowledge of law. This unique feature enable a student to make time for judgment reading.
</p>

<p>
<h4><strong>OBJECTIVE OF THE COURSE:</strong></h4>   
<hr>     
The main objective of this course is to get  law students aware of various fixed data points in judgments. These data points are always usefull for any research analysis that can pivot around the judgment or the conclusion of any case. Generating judgment element make students more consious about the judgments reading. The abstract writing of the judgment will enable a student to understand the judgment more efficiently.</p>
<p>
	<h4><strong>SYLLABUS:</strong></h4>
  <ol type="i">
	<li>Allocating Fixed Legal Data Points from the Judgments</li>
  <li>Generating Judgment Elements of the judgment</li>
  <li>Abstract Writing of the judgment</li>
  <li>Cases Referred in the judgment</li>
  </ol>
</p>
<p>
    <h4><strong>COURSE CONTENT:</strong></h4><hr>
        <ol type="i">
          <li>2000-4000 Judgments will be given.</li>
          <li>For allocating fixed data points student have to follow certain rules which are as follows:
          <ol type="a">
          	<li><b>Case Number -</b> The number of the case as provided in the judgment</li>
          	<li><b>Jurisdiction of the Court -</b> Original, appeallate, Writs, Special Leave Jurisdiction according to the case.</li>
          	<li><b>Judges Name -</b>As provided in the case.</li>
          	<li><b>Bench-</b>
          		<ul>
          			<li>Name of One judge - Single Bench</li>
          			<li>Name of Two Judges - Division Bench</li>
          			<li>Name of Three or more Judges - Full Bench</li>
          			<li>Constitution Bench- Constitution Bench</li>
          		</ul>
            </li>
            <li><b>Advocates Name - </b>As provided in the case for both the parties</li>
            <li><b>Citation - </b>As provided in the case for both the parties</li>
            <li><b>Description - </b>As per the acts and section imposed in the case</li>
            <li><b>Disposition - </b>As per the decision of the case</li>
          </ol>
          </li>
          <li>For Abstract writing there are some rules which the student have to follow, which are as follows:
            <ol type="a">
            <li>Name of the Act and Sections must be mentioned.</li>
            <li>Facts of the Case in brief.</li>
            <li>Decision of the Lower Courts, High Courts, Tribunals and Supreme Court.</li>
            <li>Abstract must be written in past tense.</li>

          </ol>
          </li>
          <li>Cases Referred
            <ol type="a">
              Describe/Mention the cases that are referred in the particular judgment. 
            </ol>
          </li>
          <li>15-20 Judgments have to be written by the student during the course period. </li>
          <li>The judgment on which the judgment elements have to be written have to be selected by the student.</li>
         <li>Judgment Elements consists of:
         <ol type="a">
           <li>Facts of the Case: Matter in issue should be mentioned</li>
           <li>Ruling: Mentioning decision of lower courts and Tribunals if any</li>
           <li>Arguments: Arguments of the advocates</li>
           <li>Cases Referred: Mention the name and description of the cases referred.Variable Data Points</li>
           <li>Decision and Reason: Decision and the reason of that decision should be mentioned of the Court under which the case was going on.</li>
           <li>Everything must be plotted according to the elements. No change should be done during plotting of the elements. </li>
         </ol> 
         </li>
        </ol>
        <b>In case any of the data points is missing in full judgment text , student has to search on web and generate the missing data points. URL of the website to be submitted in the data point remark section of the interface. Extra marks will be given for this.</b>
</p>
